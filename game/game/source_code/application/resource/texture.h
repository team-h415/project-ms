#ifndef _TEXTURE_H_
#define _TEXTURE_H_

//================================================================================
// インクルードファイル
//================================================================================
#include "../render/directx9/directx9.h"

//================================================================================
// クラス定義
//================================================================================
class Texture
{
	public:
		//================================================================================
		// コンストラクタ
		// 戻り値 : なし
		// 引数1 : テクスチャパス
		//================================================================================
		Texture(const char* pass);

		//================================================================================
		// デストラクタ
		// 戻り値 : なし
		// 引数1 : なし
		//================================================================================
		~Texture(void);

		//================================================================================
		// テクスチャポインタ取得
		// 戻り値 : テクスチャポインタ
		// 引数1 : なし
		//================================================================================
		IDirect3DTexture9* GetTexture(void){ return tex_; }

		//================================================================================
		// テクスチャパス取得
		// 戻り値 : テクスチャパス先頭ポインタ
		// 引数1 : なし
		//================================================================================
		char* GetTexturePass(void){ return tex_pass_; }

	private:
		//================================================================================
		// テクスチャ取得
		// 戻り値 :成功 失敗
		// 引数1 : なし
		//================================================================================
		bool Load(const char* pass);

		//================================================================================
		// 変数宣言
		//================================================================================
		IDirect3DTexture9*			tex_;				// テクスチャポインタ
		char*						tex_pass_;			// テクスチャパス
};

#endif